package transfer;

public class Branch {
	
	private String branchCode;
	private String bname;
	
	public Branch(String branchCode, String bname) {
		super();
		this.branchCode = branchCode;
		this.bname = bname;
	}

	public String getBranchCode() {
		return branchCode;
	}

	public String getBname() {
		return bname;
	}


	
	
}
