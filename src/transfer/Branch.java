package transfer;

public class Branch {
	
	private int branchCode;
	private String bname;
	
	public Branch(int branchCode, String bname) {
		super();
		this.branchCode = branchCode;
		this.bname = bname;
	}

	public int getBranchCode() {
		return branchCode;
	}

	public String getBname() {
		return bname;
	}


	
	
}
